	/**
	 * Display the specified resource.
	 *
	 * @param  int  $id
	 * @return \Illuminate\Http\Response
	 */
	public function show($slug) {
		$page = \App\Models\Page\Page::whereHas('translations', function ($query) use ($slug) {
			$query->where('slug', 'about-us');
		})->firstOrFail();

		if ($page->slug != $slug) {
			return redirect($page->link);
		}

		return view('frontend.pages.show')->withPage($page);
	}

